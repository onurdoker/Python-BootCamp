# with numbers
number = 10
print(number)

number += 5  # number = number + 5
print(number)

number *= 5  # number = number * 5
print(number)

number -= 5  # number = number - 5
print(number)

number **= 5  # number = number ** 5
print(number)

number %= 5  # number = number % 5
print(number)

number /= 5  # number = number / 5
print(number)

# with strings
name = "Tech"
name += "Istanbul"
print(name)  # TechIstanbul

name *= 3
print(name)  # TechIstanbulTechIstanbulTechIstanbul
