"""
OPERATORS IN PYTHON

Tools used to perform appropriate data manipulations and calculations for data types are refered to as 'operators' in Python.
"""

# Aritmatical Operators
print(3 + 4)  # 7
print(8 - 5)  # 3
print(8 * 5)  # 40
print(8 / 5)  # 1.6
# in division operations, the result is always a float value
print(10 / 5)  # 2.0

print(5**2)  # 25 -> power of 5 to 2
print(8 // 3)  # 2 -> floor division ( round down )
print(8 % 3)  #  2 -> remainder after division of 8 by 3

# With strings, we can use + operator for concatenation and * operator for repetition
print("Tech" + "Istanbul")  # TechIstanbul
print("Tech" * 5)  # TechTechTechTechTech
