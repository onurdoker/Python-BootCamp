"""
Comparison Operators
"""

# Equality Operator (==)
print("tech" == "istanbul")  # False
print("tech" == "tech")  # True

# Inequality Operator (!=)
print("tech" != "istanbul")  # True
print("tech" != "tech")  # False

# Greater Than Operator (>)
print(4 > 8)  # False
print(8 > 4)  # True

# Greater Than or Equal To Operator (>=)
print(4 >= 8)  # False
print(8 >= 4)  # True

# Less Than Operator (<)
print(4 < 8)  # True
print(8 < 4)  # False

# Less Than or Equal To Operator (<=)
print(4 <= 8)  # True
print(8 <= 4)  # False
