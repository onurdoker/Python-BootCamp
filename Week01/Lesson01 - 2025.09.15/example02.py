"""
Data types in Python

There is 4 main data types in python:
1. Integers -> int()
2. Floats -> float()
3. Strings -> str()
3. Bloeans -> bool()

Determine the data type of a variable we can use the type() function.
"""

# Integers
print(34)  # 34
print(type(34))  # <class 'int'>

# Floats
print(34.2)  # 34.2
print(-1.77)  # -1.77
print(type(34.2))  # <class 'float'>

# Strings
print("TechIstanbul")  # TechIstanbul
print(type("TechIstanbul"))  # <class 'str'>
print("""Python Bootcamp""")  # Python Bootcamp

"""
Excape characters in strings:
\' -> ' or
\" -> " or
\n -> new line
\t -> tab
"""
print('Hello \n "TechIstanbul"')
# Hello
#  "TechIstanbul"

# Booleans
print(True)  # True
print(type(True))  # <class 'bool'>

print(False)  # False
print(type(False))  # <class 'bool'>
