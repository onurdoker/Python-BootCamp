"""
Lambda Function Example
Utilize short, one-line functions for quick operations.
"""

calculate_square = lambda number: number**2

print(calculate_square(5))  # 25
print(calculate_square(10))  # 100


calculate_addition = lambda number1, number2: number1 + number2

print(calculate_addition(3, 5))  # 8
print(calculate_addition(12, 23))  # 35
